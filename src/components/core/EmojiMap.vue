    findSqIDByWorldCoords(wCoords) {
      return Math.floor(wCoords / this.scale[this.currentScale])
    },
    findSqIDByWorldCoordsMod36(wCoords) {
      return this.findSqIDByWorldCoords(wCoords) % 36
    },
    zoomIn() {
      if (!this.disableZoom) {
        this.zoom++
      }
    },
    zoomOut() {
      if (this.zoom > 3 && !this.disableZoom) {
        this.zoom--
      }
