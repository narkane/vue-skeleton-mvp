                : Math.abs(this.bounds.ne.x + (256 - this.bounds.sw.x))
            // this.mapCoordSize.width = Math.round(this.mapCoordSize.width)

            console.log(this.mapCoordSize.width)

            this.mapCoordSize.height = Math.abs(
              this.bounds.sw.y - this.bounds.ne.y
            )
            console.log(this.mapCoordSize.height)

            if (this.canvas.getContext) {
              this.drawGrid()
              this.drawEmojis(
                (this.scale[this.currentScale] * this.canvas.width) /
                  this.mapCoordSize.width
              )
            } else {
              // canvas unsupported
              // eslint-disable-next-line no-alert
              alert('CANVAS UNSUPPORTED!')
            }
          }
        })
        this.google.maps.event.addListenerOnce(
          map,
          'projection_changed',
          () => {
            if (this.$route.params.emojiCode) {
              this.center = this.convertEmojiInput(this.$route.params.emojiCode)
              this.zoomByEmojiLength(
                map,
                Math.ceil(this.$route.params.emojiCode.length / 2)
              )
              this.center.lat = map
                .getProjection()
                .fromPointToLatLng(
                  new this.google.maps.Point(this.center.lng, this.center.lat)
                )
                .lat()
              console.log(`center: ${JSON.stringify(this.center)}`)
            }
            map.setCenter(this.center)
            console.log(map.getCenter().lat(), map.getCenter().lng())
          }
        )
      })
      this.canvas.height = this.gmap.offsetHeight
      this.canvas.width = this.gmap.offsetWidth
    },
    getEmojiLocation(x, y) {
      this.emojiLocation = ''
      let xEmojiLoc = (
        this.findSqIDByWorldCoords(x) / Math.pow(36, this.currentScale)
      ).toString(36)
      let yEmojiLoc = (
        this.findSqIDByWorldCoords(y) / Math.pow(36, this.currentScale)
      ).toString(36)
      xEmojiLoc = xEmojiLoc.replace('.', '')
      yEmojiLoc = yEmojiLoc.replace('.', '')
      for (let i = 0; i <= this.breakLayer(this.zoom); i++) {
        if (xEmojiLoc[i] === undefined) {
          xEmojiLoc += 0
        }
        if (yEmojiLoc[i] === undefined) {
          yEmojiLoc += 0
        }
        this.emojiLocation += `${
          this.emojiIndexReference[xEmojiLoc[i]][yEmojiLoc[i]]
        }`
      }
      return this.emojiLocation
    },
    // eslint-disable-next-line max-statements
    boundWorld(map) {
      if (this.bounds.ne.y < 0 || this.bounds.sw.y > 256) {
        // still within valid bounds, so save the last valid position
        map.panTo(this.center)
      }
    },
    findSqIDByWorldCoords(wCoords) {
      return Math.floor(wCoords / this.scale[this.currentScale])
    },
    findSqIDByWorldCoordsMod36(wCoords) {
      return this.findSqIDByWorldCoords(wCoords) % 36
    },
    fixedZooms(map) {
      if (this.zoom > map.getZoom()) {
        if (map.getZoom() === 6) {
          map.setZoom(5)
        } else if (map.getZoom() === 11) {
          map.setZoom(10)
        } else if (map.getZoom() === 17) {
          map.setZoom(16)
        } else if (map.getZoom() === 22) {
          map.setZoom(21)
        }
      } else if (this.zoom < map.getZoom()) {
        if (map.getZoom() === 6) {
          map.setZoom(7)
        } else if (map.getZoom() === 11) {
          map.setZoom(12)
        } else if (map.getZoom() === 17) {
          map.setZoom(18)
        } else if (map.getZoom() === 22) {
          map.setZoom(21)
        }
      }
    },
    zoomByEmojiLength(map, len) {
      this.currentScale = len - 1
      console.log(len)
      if (len === 1) {
        map.setZoom(3)
      } else if (len === 2) {
        map.setZoom(7)
      } else if (len === 3) {
        map.setZoom(12)
      } else if (len === 4) {
        map.setZoom(18)
      }
      this.zoom = map.getZoom()
    },
    breakLayer(gZoom) {
      if (gZoom < 7) {
        return 0
      } else if (gZoom < 12) {
        return 1
      } else if (gZoom < 18) {
        return 2
      }
      return 3
    },
    toggleEmojis() {
      if (this.emojiToggle === 'Off') {
        this.emojiToggle = 'On'
        this.ctx.clearRect(0, 0, this.canvas.width, this.canvas.height)
        this.drawGrid()
      } else {
        this.emojiToggle = 'Off'
        // this.ctx.clearRect(0, 0, this.canvas.width, this.canvas.height)
        this.drawGrid()
        this.drawEmojis(
          (this.scale[this.currentScale] * this.canvas.width) /
            this.mapCoordSize.width
        )
      }
      console.log(this.emojiToggle)
    },
    drawEmojis(nextLineDist) {
      if (this.emojiToggle === 'Off') {
        const sqID = {
          x: this.findSqIDByWorldCoordsMod36(this.bounds.sw.x),
          // (this.bounds.sw.x + this.firstLatLineInPx() / pxPerCoord) /
          // this.scale[this.currentScale],
          y: this.findSqIDByWorldCoordsMod36(this.bounds.ne.y)
          //     (this.bounds.ne.y + this.firstLngLineInPx() / pxPerCoord) /
          //     this.scale[this.currentScale]
        }
        this.ctx.font = '12px Serif'
        for (let i = 0; i < this.canvas.width; i += nextLineDist) {
          sqID.y = this.findSqIDByWorldCoordsMod36(this.bounds.ne.y)
          for (
            let j = 0;
            j < this.canvas.height + nextLineDist;
            j += nextLineDist
          ) {
            this.ctx.fillText(
              // String.fromCodePoint(
              `${
                this.emojiIndexReference[sqID.x.toString(36)][
                  sqID.y.toString(36)
                ]
              }`,
              this.firstLatLineInPx() - nextLineDist + i - 7,
              this.firstLngLineInPx() - nextLineDist + j + 4
            )
            if (sqID.y < 35) {
              sqID.y++
            } else {
              sqID.y = 0
            }
          }
          if (sqID.x < 35) {
            sqID.x++
          } else {
            sqID.x = 0
          }
        }
      }
    },
    drawGrid() {
      const level = this.scale[this.currentScale]

      this.ctx.clearRect(0, 0, this.canvas.width, this.canvas.height)

      this.ctx.beginPath()
      // metersPerPx = 156543.03392 * Math.cos(latLng.lat() * Math.PI / 180) / Math.pow(2, zoom)
      // pxsPerMeter = Math.pow(2, this.zoom) / (156543.03392 * Math.cos(latLng.lat() * Math.PI / 180))
      // 1113174.304 mEL @ zoom = 1 */
      this.ctx.strokeStyle = `rgb(${255 / this.currentScale}, ${
        255 / this.currentScale
      }, 255)`
      const pxPerEmoji =
        1113174.304 /
        ((156543.03392 * Math.cos((this.center.lat * Math.PI) / 180)) /
          Math.pow(2, this.zoom))

      this.drawLat(level)
      this.drawLng(level)
      this.ctx.closePath()
      this.ctx.stroke()
    },
    drawLat(emojis) {
      const pxPerCoord = this.canvas.width / this.mapCoordSize.width
      for (
        let i = this.firstLatLineInPx();
        i < this.canvas.width;
        i += emojis * pxPerCoord
      ) {
        this.ctx.moveTo(i, 0)
        this.ctx.lineTo(i, this.canvas.height)
      }
    },
    drawLng(emojis) {
      const pxPerCoord = this.canvas.height / this.mapCoordSize.height
      for (
        let i = this.firstLngLineInPx();
        i < this.canvas.height;
        i += emojis * pxPerCoord
      ) {
        this.ctx.moveTo(this.canvas.width, i)
        this.ctx.lineTo(0, i)
      }
    },
    firstLatLineInPx() {
      const emojis = this.scale[this.currentScale]
      const pxPerCoord = this.canvas.width / this.mapCoordSize.width
      return (
        (Math.ceil(this.bounds.sw.x / emojis) * emojis - this.bounds.sw.x) *
        pxPerCoord
      )
    },
    firstLngLineInPx() {
      const emojis = this.scale[this.currentScale]
      const pxPerCoord = this.canvas.height / this.mapCoordSize.height
      return (
        (Math.ceil(this.bounds.ne.y / emojis) * emojis - this.bounds.ne.y) *
        pxPerCoord
      )
    },
    // The mapping between latitude, longitude and pixels is defined by the web
    // mercator projection.
    mercatorProject(latLng) {
      let siny = Math.sin((latLng.lat() * Math.PI) / 180)
      // Truncating to 0.9999 effectively limits latitude to 89.189. This is
      // about a third of a tile past the edge of the world tile.
      siny = Math.min(Math.max(siny, -0.9999), 0.9999)
      return new this.google.maps.Point(
        this.tileSize * (0.5 + latLng.lng() / 360),
        this.tileSize *
          (0.5 - Math.log((1 + siny) / (1 - siny)) / (4 * Math.PI))
      )
    },
    // receives a place object via the autocomplete component
    setPlace(place) {
      this.currentPlace = place
    },
    addMarker() {
      if (this.currentPlace) {
        const marker = {
          lat: this.currentPlace.geometry.location.lat(),
          lng: this.currentPlace.geometry.location.lng()
        }
        this.markers.push({ position: marker })
        this.places.push(this.currentPlace)
        this.center = marker
        this.currentPlace = null
      }
    },
    geolocate() {
      navigator.geolocation.getCurrentPosition((position) => {
        this.center = {
          lat: position.coords.latitude,
          lng: position.coords.longitude
        }
      })
    },
    mouseCheck() {
      const that = this
      document.onmousedown = function () {
        that.mouse.down = true
      }
      document.onmouseup = function () {
        that.mouse.down = false
      }
      document.onmousemove = function (e) {
        if (that.mouse.down) {
          // that.mouse.dir = e.pageX - that.mouse.oldX
          // that.drawGrid()
          // that.mouse.dir = 0
        }
        // that.mouse.oldX = e.pageX
      }
    },
    addRemoteLibraries() {
      const plugin = document.createElement('script')
      plugin.setAttribute(
        'src',
        `https://maps.googleapis.com/maps/api/js?key=${process.env.VUE_APP_GMAPS_API_KEY}&callback=init&libraries=&v=weekly`
      )
      plugin.async = true
      document.head.appendChild(plugin)
    }
  }
}
